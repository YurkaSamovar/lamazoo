    private void jBEnvoyerSoinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEnvoyerSoinActionPerformed
        if(!jTFTitreSoin.getText().equals("") & !jTPSoin.getText().equals("")) {
            int confirm = JOptionPane.showOptionDialog(this,
          "Volez vous envoyer un soin?", "Exit", JOptionPane.YES_NO_OPTION,
       JOptionPane.QUESTION_MESSAGE, null, null, null);
            if (confirm == JOptionPane.YES_OPTION) {
                veterinaireSQL.setSoin(jTFTitreSoin.getText(), jTPSoin.getText(),  animalId, (int)listDesSoignants.get(jCBSoignant.getSelectedIndex()).get(0));
                jTFTitreSoin.setText("");
                jTPSoin.setText("");
            } 
        } else {
            JOptionPane.showMessageDialog(null, "Remplissez la title ou la discription.");
            
        }
    }//GEN-LAST:event_jBEnvoyerSoinActionPerformed

