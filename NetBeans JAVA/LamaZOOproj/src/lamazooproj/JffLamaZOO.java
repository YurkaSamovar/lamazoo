    public JffLamaZOO(LogIn accesse) throws IOException {
        lamazoo = new Connexion("Localhost", "db_lamazoo", "publicVisiteur", "mdpVisiteur");
        initComponents();
        this.accesse = accesse;
        accesseChecker = new CheckAccesse(accesse, lamazoo);
        veterinaireSQL = new VeterinaireSQL(accesse, lamazoo);
        if(accesseChecker.check()) {
            listDesAnimals = veterinaireSQL.getListDesAnimals();
            remplirTableau(listDesAnimals);
            listDesSoignants = veterinaireSQL.getSoignant();
            remplirSoignant(listDesSoignants);
        } else {
            accesseError = true;
        }   
    }
    
