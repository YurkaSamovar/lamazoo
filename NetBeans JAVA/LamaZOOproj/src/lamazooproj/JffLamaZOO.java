    private void jTAnimeauxMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTAnimeauxMouseClicked
        if(accesseChecker.check()) {
            animalId = (int)jTAnimeaux.getValueAt(jTAnimeaux.getSelectedRow(), 0);
            animalInfo = veterinaireSQL.getInfoDesAlimeaux(animalId);
            jTFNomAnimal.setText(animalInfo.get(0).toString());
            jTFSurnomAnimal.setText(animalInfo.get(1).toString());
            jTFPoidsAnimal.setText(animalInfo.get(2).toString());
            jTFPaysAnimal.setText(animalInfo.get(3).toString());
            jTFNaissanceAnimal.setText(animalInfo.get(4).toString());
            jTFDecesAnimal.setText(animalInfo.get(5).toString());

            jTFComm.setText(String.valueOf(veterinaireSQL.getNouveauxComm(animalId)));
            styleComm();
            jBComm.setEnabled(true);
            if(jffCommentaire != null) {
                jffCommentaire.setVisible(false);
            } 
            if(jffHistoriqueSoin != null) {
                jffHistoriqueSoin.setVisible(false);
            }
            jffCommentaire = new JffCommentaire(accesse, animalId, veterinaireSQL, lamazoo, accesseChecker, jPComm, jTFComm);
            jffHistoriqueSoin = new JffHistoriqueSoin();
            jTFTitreSoin.setEnabled(true);
            jTPSoin.setEnabled(true);
            jLTitreSoin.setEnabled(true);
            jLSoin.setEnabled(true);
            jBHistorique.setEnabled(true);
            jBEnvoyerSoin.setEnabled(true);
            jCBSoignant.setEnabled(true);
        } else {
            badToken();
        }
    }//GEN-LAST:event_jTAnimeauxMouseClicked

