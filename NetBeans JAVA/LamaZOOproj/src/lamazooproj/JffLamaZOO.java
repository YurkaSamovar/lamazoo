    private void remplirTableau(HashMap<Integer, String[]> listDesAnimals) {
        DefaultTableModel model = (DefaultTableModel) jTAnimeaux.getModel();
        listDesAnimals.forEach((key, value) -> {
            model.addRow(new Object[] {key, value[1], value[0]});
        }); 
    }
    
    
    private void remplirSoignant(ArrayList<ArrayList<Object>> listDesSoignants) {
        for(ArrayList<Object> listObject : listDesSoignants) {
            jCBSoignant.addItem(String.valueOf(listObject.get(1)) + " " + String.valueOf(listObject.get(2)));
        } 
    }
    
